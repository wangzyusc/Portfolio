function ViewPedestrianDetail(){
    $("#pedestrian").css("display", "block");
    $("#viewpedestrian").css("display", "none");
    $("#hidepedestrian").css("display", "block");
}

function HidePedestrianDetail(){
    $("#pedestrian").css("display", "none");
    $("#viewpedestrian").css("display", "block");
    $("#hidepedestrian").css("display", "none");
}